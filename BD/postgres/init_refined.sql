-- Etapa 1: Siembra
CREATE TABLE RAM_ETAPA1_SIEMBRA (
    CODIGO_ALI BIGINT PRIMARY KEY REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    AGAR_PLATE_COUNT VARCHAR(50),
    ID_EQUIPO_INCUBACION INT REFERENCES EQUIPOS_INCUBACION(ID_INCUBACION),
    -- Se mantiene aquí por flujo UI
    HORA_INICIO_HOMOGENIZADO VARCHAR(10),
    HORA_TERMINO_SIEMBRA VARCHAR(10),
    MICROPIPETA_UTILIZADA VARCHAR(100),
    N_MUESTRA_10GR VARCHAR(100),
    N_MUESTRA_50GR VARCHAR(100)
);
-- Etapa 2: Incubación
CREATE TABLE RAM_ETAPA2_INCUBACION (
    CODIGO_ALI BIGINT PRIMARY KEY REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    ID_RESPONSABLE_ANALISIS VARCHAR(20) REFERENCES USUARIOS(RUT_ANALISTA),
    ID_RESPONSABLE_INCUBACION VARCHAR(20) REFERENCES USUARIOS(RUT_ANALISTA),
    FECHA_INICIO_INCUBACION DATE,
    FECHA_FIN_INCUBACION DATE,
    HORA_INICIO_INCUBACION VARCHAR(10),
    HORA_FIN_INCUBACION VARCHAR(10)
);
-- Etapa 3: Muestras (Cálculos directos)
CREATE TABLE RAM_ETAPA3_MUESTRAS (
    ID_MUESTRA SERIAL PRIMARY KEY,
    CODIGO_ALI BIGINT NOT NULL REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    NUMERO_MUESTRA INT NOT NULL,
    DIL_1 NUMERIC(10, 5),
    DIL_2 NUMERIC(10, 5),
    C1 INT,
    C2 INT,
    C3 INT,
    C4 INT,
    RESULTADO_RAM VARCHAR(100),
    RESULTADO_RPES VARCHAR(100),
    PROMEDIO_COLONIAS VARCHAR(100),
    N1 INT,
    N2 INT,
    SUMATORIA_COLONIAS INT,
