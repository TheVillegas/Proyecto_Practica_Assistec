-- Etapa 3: Duplicados
CREATE TABLE RAM_ETAPA3_DUPLICADO (
    CODIGO_ALI BIGINT PRIMARY KEY REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    ID_MUESTRA_ORIGINAL INT REFERENCES RAM_ETAPA3_MUESTRAS(ID_MUESTRA) ON DELETE CASCADE,
    DIL_DUP_1 NUMERIC(10, 5),
    DIL_DUP_2 NUMERIC(10, 5),
    C_DUP_1 INT,
    C_DUP_2 INT,
    C_DUP_3 INT,
    C_DUP_4 INT,
    RESULTADO_RAM_DUP VARCHAR(100),
    RESULTADO_RPES_DUP VARCHAR(100),
    PROMEDIO_COLONIAS_DUPLICADO VARCHAR(100),
    N1_DUPLICADO INT,
    N2_DUPLICADO INT,
    SUMATORIA_COLONIAS_DUPLICADO INT,
    FACTOR_DILUCION NUMERIC(15, 2)
);
-- Etapa 4: Lectura
CREATE TABLE RAM_ETAPA4_LECTURA (
    CODIGO_ALI BIGINT PRIMARY KEY REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    HORA_INICIO_LECTURA VARCHAR(10),
    HORA_FIN_LECTURA VARCHAR(10),
    TEMPERATURA NUMERIC(5, 2),
    BLANCO_UFC INT,
    CONTROL_UFC INT,
    CONTROL_SIEMBRA_ECOLI INT,
    CONTROL_AMBIENTAL_PESADO NUMERIC(15, 2),
    -- Recuperado de Oracle
    UFC_CONTROL_PESADO NUMERIC(15, 2)
);
-- Etapa 5: Verificación
CREATE TABLE RAM_ETAPA5_VERIFICACION (
    CODIGO_ALI BIGINT PRIMARY KEY REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    LIMITE VARCHAR(50),
    MERCADO VARCHAR(100),
    FECHA_ENTREGA DATE,
    HORA_ENTREGA VARCHAR(10),
    TABLA_PAGINA VARCHAR(50),
    DESFAVORABLE VARCHAR(10),
    -- "SI" o "NO"
    MANUAL_INOCUIDAD TEXT -- Base64 o URL de la imagen del manual
);
-- Etapa 6: Aseguramiento
CREATE TABLE RAM_ETAPA6_ASEGURAMIENTO (
    CODIGO_ALI BIGINT PRIMARY KEY REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    ID_ANALISIS_DUPLI INT REFERENCES MAESTRO_TIPOS_ANALISIS(ID_TIPO_ANALISIS),
    CONTROL_BLANCO_VAL VARCHAR(50),
    CONTROL_BLANCO_ESTADO VARCHAR(20),
    CONTROL_SIEMBRA_VAL VARCHAR(50),
    CONTROL_SIEMBRA_ESTADO VARCHAR(20),
    DUPLICADO_ALI_VAL VARCHAR(50),
    DUPLICADO_ESTADO VARCHAR(20)
