-- Etapa 7: Cierre y Formas de Cálculo
CREATE TABLE RAM_ETAPA7_CIERRE (
    CODIGO_ALI BIGINT PRIMARY KEY REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    OBSERVACIONES_FINALES TEXT,
    OBSERVACIONES_GENERALES_ANALISTA_RAM TEXT
);
CREATE TABLE RAM_ETAPA7_FORMAS_CALCULO (
    ID_REGISTRO SERIAL PRIMARY KEY,
    CODIGO_ALI BIGINT NOT NULL REFERENCES RAM_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    ID_FORMA INT NOT NULL REFERENCES MAESTRO_FORMAS_CALCULO(ID_FORMA),
    TIPO_USUARIO VARCHAR(20) NOT NULL -- 'ANALISTA' o 'COORDINADOR'
);
-- --------------------------------------------------------
-- 5. VISTAS Y UTILIDADES
-- --------------------------------------------------------
CREATE OR REPLACE VIEW V_CATALOGO_UNIFICADO AS
SELECT id_instrumento AS id_origen,
    nombre_instrumento AS nombre,
    codigo_instrumento AS codigo,
    'INSTRUMENTO' AS tipo_material,
    NULL::VARCHAR AS capacidad
FROM INSTRUMENTOS
UNION ALL
SELECT id_pipeta AS id_origen,
    nombre_pipeta || ' (' || capacidad || ')' AS nombre,
    codigo_pipeta AS codigo,
    'PIPETA' AS tipo_material,
    capacidad
FROM MICROPIPETAS
ORDER BY nombre ASC;
