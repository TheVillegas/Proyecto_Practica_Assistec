-- 2. TABLAS MAESTRAS (Catálogos)
-- --------------------------------------------------------
CREATE TABLE DILUYENTES (
    ID_DILUYENTE SERIAL PRIMARY KEY,
    NOMBRE_DILUYENTE VARCHAR(100) NOT NULL,
    MILILITROS VARCHAR(50) NOT NULL
);
CREATE TABLE EQUIPOS_INCUBACION (
    ID_INCUBACION SERIAL PRIMARY KEY,
    NOMBRE_EQUIPO VARCHAR(100) NOT NULL,
    TEMPERATURA_REF VARCHAR(50)
);
CREATE TABLE EQUIPOS_LAB (
    ID_EQUIPO SERIAL PRIMARY KEY,
    NOMBRE_EQUIPO VARCHAR(100) NOT NULL,
    CODIGO_EQUIPO VARCHAR(50)
);
CREATE TABLE INSTRUMENTOS (
    ID_INSTRUMENTO SERIAL PRIMARY KEY,
    NOMBRE_INSTRUMENTO VARCHAR(100) NOT NULL,
    CODIGO_INSTRUMENTO VARCHAR(50)
);
CREATE TABLE LUGARES_ALMACENAMIENTO (
    ID_LUGAR SERIAL PRIMARY KEY,
    NOMBRE_LUGAR VARCHAR(100) NOT NULL,
    CODIGO_LUGAR VARCHAR(20)
);
CREATE TABLE MAESTRO_CHECKLIST_LIMPIEZA (
    ID_ITEM INT PRIMARY KEY,
    NOMBRE_ITEM VARCHAR(100) NOT NULL,
    DEF_SELECCIONADO INT DEFAULT 0,
    DEF_BLOQUEADO INT DEFAULT 0
);
CREATE TABLE MAESTRO_FORMAS_CALCULO (
    ID_FORMA INT PRIMARY KEY,
    NOMBRE_FORMA VARCHAR(100) NOT NULL,
    DEF_SELECCIONADO INT DEFAULT 0
);
CREATE TABLE MAESTRO_TIPOS_ANALISIS (
    ID_TIPO_ANALISIS SERIAL PRIMARY KEY,
    NOMBRE_ANALISIS VARCHAR(100) NOT NULL
);
CREATE TABLE MATERIAL_SIEMBRA (
    ID_MATERIAL_SIEMBRA SERIAL PRIMARY KEY,
    NOMBRE_MATERIAL VARCHAR(100) NOT NULL,
    DETALLE_MEDIDA VARCHAR(100)
);
CREATE TABLE MICROPIPETAS (
    ID_PIPETA SERIAL PRIMARY KEY,
    NOMBRE_PIPETA VARCHAR(100) NOT NULL,
    CODIGO_PIPETA VARCHAR(50) NOT NULL,
    CAPACIDAD VARCHAR(20)
);
CREATE TABLE USUARIOS (
    RUT_ANALISTA VARCHAR(20) PRIMARY KEY,
    NOMBRE_APELLIDO_ANALISTA VARCHAR(100) NOT NULL,
    CORREO_ANALISTA VARCHAR(100) UNIQUE NOT NULL,
    CONTRASENA_ANALISTA VARCHAR(100) NOT NULL,
    ROL_ANALISTA INT DEFAULT 0 -- 0: Analista, 1: Supervisor/Admin
);
-- --------------------------------------------------------
-- 3. TABLAS OPERACIONALES (Muestras y TPA)
-- --------------------------------------------------------
CREATE TABLE MUESTRAS_ALI (
    CODIGO_ALI BIGINT PRIMARY KEY,
    CODIGO_OTROS VARCHAR(50),
    OBSERVACIONES_CLIENTE TEXT,
    OBSERVACIONES_GENERALES TEXT,
    FECHA_CREACION TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE TPA_REPORTE (
    CODIGO_ALI BIGINT PRIMARY KEY REFERENCES MUESTRAS_ALI(CODIGO_ALI) ON DELETE CASCADE,
    ID_LUGAR INT REFERENCES LUGARES_ALMACENAMIENTO(ID_LUGAR),
    OBSERVACIONES_INGRESO TEXT,
    ESTADO_ACTUAL VARCHAR(20) DEFAULT 'NO_REALIZADO',
    FECHA_CIERRE TIMESTAMP,
    USUARIO_CIERRE VARCHAR(20) REFERENCES USUARIOS(RUT_ANALISTA),
    OBSERVACIONES_FINALES TEXT,
    FIRMA_DIGITAL TEXT,
    OBSERVACIONES_GENERALES_ANALISTAS TEXT,
    FECHA_ULTIMA_MODIFICACION TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    USUARIO_ULTIMA_MODIFICACION VARCHAR(20) REFERENCES USUARIOS(RUT_ANALISTA)
);
CREATE TABLE TPA_ETAPA2_SESION (
    ID_SESION SERIAL PRIMARY KEY,
    CODIGO_ALI BIGINT NOT NULL REFERENCES TPA_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    RUT_ANALISTA VARCHAR(20) REFERENCES USUARIOS(RUT_ANALISTA),
    FECHA_INICIO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FECHA_TERMINO TIMESTAMP,
    NUMERO_MUESTRAS VARCHAR(50),
    OBSERVACIONES_SESION TEXT,
    RETIRO_PESADO INT DEFAULT 0,
    LUGAR_ALMACENAMIENTO VARCHAR(100)
);
CREATE TABLE TPA_ETAPA2_EQUIPOS (
    ID_USO_EQUIPO SERIAL PRIMARY KEY,
    ID_SESION INT NOT NULL REFERENCES TPA_ETAPA2_SESION(ID_SESION) ON DELETE CASCADE,
    ID_EQUIPO INT NOT NULL REFERENCES EQUIPOS_LAB(ID_EQUIPO)
);
CREATE TABLE TPA_ETAPA2_MATERIALES (
    ID_USO_MATERIAL SERIAL PRIMARY KEY,
    ID_SESION INT NOT NULL REFERENCES TPA_ETAPA2_SESION(ID_SESION) ON DELETE CASCADE,
    TIPO_MATERIAL VARCHAR(20) NOT NULL,
    ID_PIPETA INT REFERENCES MICROPIPETAS(ID_PIPETA),
    ID_INSTRUMENTO INT REFERENCES INSTRUMENTOS(ID_INSTRUMENTO),
    CODIGO_MANUAL VARCHAR(100)
);
CREATE TABLE TPA_ETAPA3_CHECKLIST (
    ID_CHECKLIST SERIAL PRIMARY KEY,
    CODIGO_ALI BIGINT NOT NULL REFERENCES TPA_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    NOMBRE_ITEM VARCHAR(100) NOT NULL,
    SELECCIONADO INT DEFAULT 0,
    BLOQUEADO INT DEFAULT 0
);
CREATE TABLE TPA_ETAPA4_RETIRO (
    ID_RETIRO SERIAL PRIMARY KEY,
    CODIGO_ALI BIGINT NOT NULL REFERENCES TPA_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    RUT_ANALISTA VARCHAR(20) REFERENCES USUARIOS(RUT_ANALISTA),
    FECHA_RETIRO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    ACCION_REALIZADA TEXT,
    HORA_INICIO VARCHAR(10)
);
CREATE TABLE TPA_ETAPA5_SIEMBRA (
    ID_SIEMBRA SERIAL PRIMARY KEY,
    CODIGO_ALI BIGINT UNIQUE NOT NULL REFERENCES TPA_REPORTE(CODIGO_ALI) ON DELETE CASCADE,
    OBSERVACIONES_SIEMBRA TEXT,
    OTROS_EQUIPOS_TEXTO VARCHAR(500),
    FECHA_REGISTRO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE TPA_ETAPA5_RECURSOS (
    ID_RECURSO_SIEMBRA SERIAL PRIMARY KEY,
    ID_SIEMBRA INT NOT NULL REFERENCES TPA_ETAPA5_SIEMBRA(ID_SIEMBRA) ON DELETE CASCADE,
    CATEGORIA_RECURSO VARCHAR(20) NOT NULL CHECK (
        CATEGORIA_RECURSO IN ('MATERIAL', 'DILUYENTE', 'EQUIPO')
    ),
    ID_MATERIAL_SIEMBRA INT REFERENCES MATERIAL_SIEMBRA(ID_MATERIAL_SIEMBRA),
    CODIGO_MATERIAL VARCHAR(100),
    ID_DILUYENTE INT REFERENCES DILUYENTES(ID_DILUYENTE),
    ID_INCUBACION INT REFERENCES EQUIPOS_INCUBACION(ID_INCUBACION),
    CODIGO_DILUYENTE VARCHAR(100),
    SELECCIONADO INT DEFAULT 1,
    ID_EQUIPO INT REFERENCES EQUIPOS_LAB(ID_EQUIPO)
);
